#   In Conditional Statements, there are alot of Logical Operators
# at your disposal.
# 
# You can use your standard logical operators: "and," "or."
# You can also use a operator called "in."
#
#   Basically, the "in" operator checks if a certain string exists
# inside of something.
#
# EXAMPLE:
# if 'aul' in 'grault':         #Truthy
#... print('yes')
#
# if 'quux' in ['foo','bar','baz']:         #Falsy
#... print('yes')